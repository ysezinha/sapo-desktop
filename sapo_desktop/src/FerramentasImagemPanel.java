    public void doOk() {
    	boolean mudaImagem = true;
    	if (sapo.frmImagem.op.equals("crop") && (sapo.frmImagem.cropDialog != null)) {
    		sapo.frmImagem.cropDialog.setVisible(false);
    		sapo.frmImagem.cropDialog.dispose();
    		sapo.frmImagem.desacoplar(sapo.numImg);
    	}
    	if (sapo.frmImagem.op.equals("crop") || sapo.frmImagem.op.equals("transpose"))
    		mudaImagem = confirmaAlteracao();
    	if (mudaImagem && (sapo.numImg > 0)) {
			sapo.frmImagem.pimg=sapo.jif[sapo.numImg].getRenderedImage();
			sapo.frmImagem.editou = false;
			sapo.saveImage(sapo.jif[sapo.numImg].file.getPath());
			//sapo.desejaSalvarImagem();
		}
    	else {
    		sapo.jif[sapo.numImg].setRenderedImage(sapo.frmImagem.pimg);
    		sapo.showInternalFrameWithImage(sapo.numImg);
    	}
		setEnabledAll(true);
		jbtnOkImage.setEnabled(true);
		sapo.frmImagem.inicializaParametros();
	}

	private boolean confirmaAlteracao() {
		boolean retorno = true;
		if ((sapo.numImg > 0) && (!sapo.paciente.dados.imgData[sapo.numImg].pontosList.isEmpty())) {
			Object[] options = {"Sim","Não"};
			int result = JOptionPane.showOptionDialog(sapo,
					"Esta imagem foi contém pontos previamente marcados!\n" +
					"Esta operação irá apagar todos os pontos marcados.\n" +
					"Deseja confirmar a alteração na imagem?  ",
					"Aviso",JOptionPane.YES_NO_OPTION,
					JOptionPane.QUESTION_MESSAGE,
					null, options, options[0]);
			if (result == JOptionPane.NO_OPTION) retorno = false;
			else sapo.paciente.dados.imgData[sapo.numImg].pontosList.clear();
		}
		return retorno;
	}

