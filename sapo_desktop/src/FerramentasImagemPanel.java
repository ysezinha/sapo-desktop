    public void doCancel() {
    	setEnabledAll(true);
    	sapo.jif[sapo.numImg].setRenderedImage(sapo.frmImagem.pimg);
    	sapo.showInternalFrameWithImage(sapo.numImg);
    	sapo.frmImagem.inicializaParametros();
    	if (sapo.frmImagem.cropDialog != null) {
    		sapo.frmImagem.cropDialog.setVisible(false);
    		sapo.frmImagem.cropDialog.dispose();
    		sapo.frmImagem.desacoplar(sapo.numImg);
    	}
	}

	private void kernelBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kernelBoxActionPerformed
    	if( sapo.numImg > 0 ) {
    		int i = kernelBox.getSelectedIndex();
    		// Set the appropriate kernel and do Gradient
    		int tmp = i * 2;
    		sapo.frmImagem.initKernels();
    		sapo.frmImagem.kern_h = sapo.frmImagem.kernels[tmp];
    		sapo.frmImagem.kern_v = sapo.frmImagem.kernels[tmp+1];
    		sapo.frmImagem.gradient();
    	}
    }//GEN-LAST:event_kernelBoxActionPerformed

